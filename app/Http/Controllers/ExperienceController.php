    if ($request->has('end_job_checkbox')) {
        $data['end_job'] = now(); // Use Laravel's now() helper to get the current date
        $data['is_currently_working'] = true; // Use Laravel's now() helper to get the current date
    }
        Experience::create($data);

        return redirect()->route('experience.index')->with('success', 'The experience was added successfully!');
    }

